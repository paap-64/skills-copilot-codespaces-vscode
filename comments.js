// Create web server
var express = require('express');
var app = express();
// Create a route
app.get('/', function(req, res) {
  // Send a response to the client
  res.send('Hello World!');
});
// Listen on port 3000
app.listen(3000, function() {
  console.log('Example app listening on port 3000!');
});
